public class MathFunciones {
  public static void main(String[] args) {
    System.out.println("Algunas Funciones Estaticas de la Clase Math:");
    System.out.println("Math.min(2,3): " + Math.min(2, 3));
    System.out.println("Math.max(2,3): " + Math.max(2, 3) );
    System.out.println("Math.abs(-2.3):" + Math.abs(-2.3));
    System.out.println("Math.sqrt(4): " + Math.sqrt(4));
    System.out.println("Math.pow(2,3): " + Math.pow(2, 3));
    System.out.println("Math.ceil(2.3): " + Math.ceil(2.3));
    System.out.println("Math.cos(2 * Math.PI): " + Math.cos(2 * Math.PI));
    System.out.println("Math.round(2.3): " + Math.round(2.3));
  } 
}
